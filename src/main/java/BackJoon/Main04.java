package BackJoon;

import java.util.Scanner;

public class Main04 {
    public static void main(String[] args) {

        int A = 0;
        int B = 0;

        Scanner scanner = new Scanner(System.in);

        A = scanner.nextInt();
        B = scanner.nextInt();

        System.out.println(A + B);
        System.out.println(A - B);
        System.out.println(A * B);
        System.out.println(A / B);
        System.out.println(A % B);
    }
}
